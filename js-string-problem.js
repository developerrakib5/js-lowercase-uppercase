// Case Javascript Code
let userName = 'blackPink';
let userInput = 'blackpinK';
// toLowerCase
if (userName.toLowerCase() === userInput.toLowerCase()){
    console.log('Valid User toLowerCase');
}
else{
    console.log('Invalid User');
}
// toUpperCase
if (userName.toUpperCase() === userInput.toUpperCase()){
    console.log('Valid User toUpperCase');
}
else{
    console.log('Invalid User');
}
// toLocaleLowerCase
if (userName.toLocaleLowerCase() === userInput.toLocaleLowerCase()){
    console.log('Valid User LocaleLowerCase');
}
else{
    console.log('Invalid User');
}
// toUpperCase
if (userName.toLocaleUpperCase() === userInput.toLocaleUpperCase()){
    console.log('Valid User toLocaleUpperCase');
}
else{
    console.log('Invalid User');
}